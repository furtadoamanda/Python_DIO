nome = "Guilherme Arthur de Carvalho"

print(nome[0]) # índice 0

print(nome[-1]) # último item

print(nome[:9]) # até o 9

print(nome[10:]) # a partir do 10

print(nome[10:16]) # do 10 ao 16

print(nome[10:16:2]) # pulando de 2 em 2

print(nome[ : ]) # string inteira

print(nome[::-1]) # espelhamento

